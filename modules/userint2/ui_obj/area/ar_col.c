static rgb_colour get_area_ui_object_colour (ui_object *obj)
{
	
	area_ui_object
		*area;

	rgb_colour
		col;

	int
		dr,
		dg,
		db,
		da;

	float
		t;

	//
	// Calculate the T value
	//

	t = get_ui_object_bounded_t_value (obj);

	//
	// Interpolate the colour values
	//

	area = obj->data;

	dr = ( int ) area->colour_end.r - ( int ) area->colour_start.r;
	dg = ( int ) area->colour_end.g - ( int ) area->colour_start.g;
	db = ( int ) area->colour_end.b - ( int ) area->colour_start.b;
	da = ( int ) area->colour_end.a - ( int ) area->colour_start.a;

	dr *= t;
	dg *= t;
	db *= t;
	da *= t;

	col.r = ( ( int ) area->colour_start.r + dr );
	col.g = ( ( int ) area->colour_start.g + dg );
	col.b = ( ( int ) area->colour_start.b + db );
	col.a = ( ( int ) area->colour_start.a + da );

	return (col);
}

