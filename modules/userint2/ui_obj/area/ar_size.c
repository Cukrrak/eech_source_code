static void set_area_ui_object_x_size (ui_object *obj, float x_size)
{
	
	area_ui_object
		*area;

	area = obj->data;

	area->x_size_end = x_size;
	area->x_size_start = x_size;

	set_ui_object_redraw (obj, TRUE);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static float get_area_ui_object_x_size (ui_object *obj)
{

	float
		size;
	
	area_ui_object
		*area;

	area = obj->data;

	if (area->time_length != 0.0)
	{

		size = area->x_size_start + ((area->x_size_end - area->x_size_start) * area->t) / area->time_length;
	}
	else
	{

		size = area->x_size_start;
	}

	if (area->virtual_coords)
	{

		ui_object
			*parent;

		parent = get_ui_object_parent (obj);

		if (parent)
		{

			size *= get_ui_object_x_size (parent);
		}
		else
		{

			size *= get_screen_width (video_screen);
		}
	}

	return size;
}

