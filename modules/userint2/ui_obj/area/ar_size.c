static float get_area_ui_object_virtual_x_size (ui_object *obj)
{
	
	area_ui_object
		*area;

	area = obj->data;

	if (area->virtual_coords)
	{

		return (area->x_size_start);
	}
	else
	{

		return (area->x_size_start / get_ui_object_x_size (obj));
	}
}

