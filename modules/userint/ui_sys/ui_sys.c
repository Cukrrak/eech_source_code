/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int
	ui_installed = FALSE;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

ui_object
   *ui_objects = NULL,
   *first_free_ui_object = NULL,
   *first_used_ui_object = NULL;

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void initialise_system_ui_objects (int num_ui_object);

static void deinitialise_system_ui_objects (void);

static void repaint_ui_system (int activate);

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void initialise_ui_system (int num_ui_objects)
{

	set_ui_installed (TRUE);

   if (!get_memory_block_system_installed ())
   {

      debug_fatal ("Initialising UI system, Memory Blocks not installed");
   }

   initialise_system_ui_objects (num_ui_objects);

   initialise_ui_global_functions ();

   register_post_activate_message_function (repaint_ui_system);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void deinitialise_ui_system (void)
{

	set_ui_installed (FALSE);

   deinitialise_system_ui_objects ();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void initialise_system_ui_objects (int num_ui_objects)
{

   int
      size,
      i;

   ASSERT ((num_ui_objects > 0) && (num_ui_objects < 65536));

   size = num_ui_objects * sizeof (ui_object);

   ui_objects = malloc_heap_mem (size);

   memset (ui_objects, 0, size);

   for (i = 0; i < num_ui_objects; i++)
   {

      if (i < num_ui_objects - 1)
      {

         ui_objects [i].succ = &ui_objects [i + 1];
      }

      if (i > 0)
      {

         ui_objects [i].pred = &ui_objects [i - 1];
      }
   }

   first_free_ui_object = ui_objects;

   first_used_ui_object = NULL;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void deinitialise_system_ui_objects (void)
{

   ui_object
      *this_object;

   this_object = first_used_ui_object;

   while (this_object)
   {

      this_object = first_used_ui_object->succ;

      destroy_ui_object (first_used_ui_object);

      first_used_ui_object = this_object;
   }

   if (first_used_ui_object)
   {

      debug_log ("WARNING! ui_objects still in use");
   }

   free_mem (ui_objects);

   ui_objects = NULL;

   first_free_ui_object = NULL;

   first_used_ui_object = NULL;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

ui_object *get_free_ui_object (void)
{

   ui_object
      *new;

   new = first_free_ui_object;

   if (new)
   {

      // unlink new ui_object from free list

      first_free_ui_object = new->succ;

      if (new->succ)
      {

         new->succ->pred = NULL;
      }

      // insert new ui_object into used list

      new->succ = first_used_ui_object;

      if (new->succ)
      {

         new->succ->pred = new;
      }

      first_used_ui_object = new;
   }
   else
   {

      debug_log ("WARNING! Failed to get a free ui_object");
   }

   return (new);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_free_ui_object (ui_object *obj)
{

   ASSERT (obj);

   obj->type = UI_TYPE_UNKNOWN;

   obj->data = NULL;

   // unlink ui_objects from used list

   if (obj->pred)
   {

      obj->pred->succ = obj->succ;
   }
   else
   {

      first_used_ui_object = obj->succ;
   }

   if (obj->succ)
   {

      obj->succ->pred = obj->pred;
   }

   // link ui_objects into start of free list

   obj->succ = first_free_ui_object;

   if (obj->succ)
   {

      obj->succ->pred = obj;
   }

   obj->pred = NULL;

   first_free_ui_object = obj;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void repaint_ui_system (int activate)
{

   ui_object
      *screen_stack;

	if ( activate )
	{

		if ( !bExiting )
		{
	
	
			//
			// Need to repaint the ui screen
			//
	
			screen_stack = get_ui_screen_stack_head ();
	
			if (screen_stack)
			{
	
				set_ui_object_redraw (screen_stack, TRUE);
			}
		}
	}
}

