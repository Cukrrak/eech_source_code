struct message_table_struct
{
    int
        message_id;

    void
        ( * function ) ( long dummy );
};

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static int
    global_message_id = 0;

struct message_table_struct
    message_table[MAX_MESSAGE_FUNCTIONS];

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int get_unique_message_id ( void )
{

    global_message_id ++;

    return ( global_message_id );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//
// Better system thread calling convention. Rather than having ID's and switches, just call system_thread_function
//

static int
	system_thread_calling_id,
	system_thread_return_code;

static int
	( *system_thread_function_pointer ) ( void *data ) = NULL;
static SDL_mutex *sleeper = NULL;

static long system_thread_system_function ( void *data );

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void initialise_system_thread_calling_function ( void )
{

	system_thread_calling_id = get_unique_message_id ();
	sleeper = SDL_CreateMutex();
	SDL_LockMutex(sleeper);

	register_user_message_function ( system_thread_calling_id, system_thread_system_function );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int system_thread_function ( int ( *function ) ( void * ), void *data )
{

//	system_thread_return_code = function ( data );

	if ( SDL_ThreadID() == system_thread_id )
	{

		system_thread_return_code = function ( data );
	}
	else
	{
		SDL_Event
			event;
			
		event.type = SDL_USEREVENT;
		event.user.code = system_thread_calling_id; //application_window;
		event.user.data1 = function;
		event.user.data2 = data;

		system_thread_function_pointer = function;

		SDL_PushEvent( &event );
		/*
		* WARNING: This is a hack. Linux lacks a resemblance to the SendMessgae function
		* (push a message, wait for an answer message and continue). This is how it is supposed
		* to work: We start with the mutex locked. When we come here, we try to lock it once again.
		* This will fail, and the thread will suspend. Meanwhile, the system thread will get the
		* message = nullptr, run the code, and unlock the mutex. This will wake the sleeping thread,
		* which will once again lock the thread and continue.
		*
		* This hack works because a standard linux mutex can be locked and unlocked from any thread.
		* This is different from say, windows, where the same thread cannot lock a mutex twice.
		*
		* Another thing... This function seems to be a windows hack in itself - things will break
		* horrible in windows if you try to do certain things (like graphics) from any other
		* thread than the main thread. Do we even need this in linux?
		*/
		SDL_LockMutex(sleeper);

//		SendMessage ( application_window, WM_USER, system_thread_calling_id, (LPARAM) data );
	}

	return ( system_thread_return_code );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

long system_thread_system_function ( void *data )
{
	system_thread_return_code = system_thread_function_pointer ( data );
	SDL_UnlockMutex(sleeper);

	return ( 0 );
}

