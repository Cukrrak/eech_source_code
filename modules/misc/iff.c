float iff_float_read ( FILE *fp )
{

	unsigned char
		buffer1[4];

	unsigned char
		buffer2[4];

	float
		*result;

	fread ( buffer1, 4, 1, fp );

	buffer2[0] = buffer1[3];
	buffer2[1] = buffer1[2];
	buffer2[2] = buffer1[1];
	buffer2[3] = buffer1[0];

	result = ( float * ) buffer2;

	return ( *result );
}

