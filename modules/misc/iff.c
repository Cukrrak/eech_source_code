short int iff_short_int_read ( FILE *fp )
{

	short int
		value;

	unsigned char
		val;

	fread ( &val, 1, 1, fp );
	value = ( val<<8 );

	fread ( &val, 1, 1, fp );
	value |= ( val );

	return ( value );
}

