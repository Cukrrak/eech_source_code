void iff_float_write ( float value, FILE *fp )
{

	unsigned char
		*ptr,
		buffer[4];

	ptr = ( unsigned char * ) &value;

	buffer[0] = ptr[3];
	buffer[1] = ptr[2];
	buffer[2] = ptr[1];
	buffer[3] = ptr[0];

	fwrite ( buffer, 4, 1, fp );
}

