enum IFF_HEADER_TYPES get_iff_type ( FILE *fp )
{

	char
		buf[5];

	if ( !fp )
	{

		return ( NOT_RECOGNISED );
	}

	fread ( buf, 4, 1, fp );

	buf[4] = '\0';

	return ( get_iff_type_from_string ( buf ) );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum IFF_HEADER_TYPES get_iff_type_from_string ( char *buf )
{

	int
		recognised,
		type,
		i;

	type = NOT_RECOGNISED;

	recognised = FALSE;

	i = 0;

	while ( !recognised )
	{

		if ( iff_headers[i].id == NOT_RECOGNISED )
		{

			recognised = TRUE;

			break;
		}
		else if ( ( strcmp ( buf, iff_headers[i].name ) ) == 0 )
		{

			recognised = TRUE;

			type = iff_headers[i].id;
		}
		else
		{

			i++;
		}
	}

	return ( type );
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void put_iff_type ( FILE *fp, enum IFF_HEADER_TYPES type )
{

	int
		i;

	ASSERT ( fp );

	i = 0;

	while ( TRUE )
	{

		if ( ( iff_headers[i].id == NOT_RECOGNISED ) || ( iff_headers[i].id == type ) )
		{

			break;
		}
		else
		{

			i++;
		}
	}

	if ( iff_headers[i].id == type )
	{

		fwrite ( iff_headers[i].name, 4, 1, fp );
	}
}

