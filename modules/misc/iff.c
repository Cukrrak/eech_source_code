int iff_int_read ( FILE *fp )
{

	unsigned int
		value;

	unsigned char
		val;

	fread ( &val, 1, 1, fp );
	value = ( val<<24 );

	fread ( &val, 1, 1, fp );
	value += ( val<<16 );

	fread ( &val, 1, 1, fp );
	value += ( val<<8 );

	fread ( &val, 1, 1, fp );
	value += ( val );

	return ( value );
}

