int update_entity_simple_keyframed_value( entity *en, float *animation_state, float animation_rate )
{

	float
		state;

	int
		wrapped;

	ASSERT( en );

	state = (*animation_state) + ( animation_rate * get_delta_time() );

	//
	// wrap around
	//

	wrapped = FALSE;

	while ( state >= 1.0 )
	{
		state -= 1.0;

		wrapped = TRUE;
	}

	*animation_state = state;

	return wrapped;
}

