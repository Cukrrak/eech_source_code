typedef enum
{
	KEYFRAME_STATE_START,
	KEYFRAME_STATE_ANIMATING_FORWARDS,
	KEYFRAME_STATE_END,
	KEYFRAME_STATE_ANIMATING_REVERSE,

	NUM_KEYFRAME_ANIMATING_STATES

}	keyframe_animation_states;

