void animate_entity_reversable_keyframed_sub_objects( object_3d_instance *inst3d, object_3d_sub_object_index_numbers sub_object_type, float animation_state )
{

	int
		int_state;

	float
		anim_scale;

	if ( !inst3d )
	{
		return;
	}

	get_keyframed_animation_state( animation_state, &int_state, &anim_scale );

	switch ( int_state )
	{
		//////////////////////////////////////////////////
		case KEYFRAME_STATE_START:
		//////////////////////////////////////////////////
		{
			animate_keyframed_sub_object_type( inst3d, sub_object_type, 0.0 );
			break;
		}
		//////////////////////////////////////////////////
		case KEYFRAME_STATE_END:
		//////////////////////////////////////////////////
		{
			animate_keyframed_sub_object_type( inst3d, sub_object_type, 1.0 );
			break;
		}
		//////////////////////////////////////////////////
		case KEYFRAME_STATE_ANIMATING_FORWARDS:
		//////////////////////////////////////////////////
		{
			animate_keyframed_sub_object_type( inst3d, sub_object_type, anim_scale );
			break;
		}
		//////////////////////////////////////////////////
		case KEYFRAME_STATE_ANIMATING_REVERSE:
		//////////////////////////////////////////////////
		{
			animate_keyframed_sub_object_type( inst3d, sub_object_type, 1.0 - anim_scale );
			break;
		}
	}
}

