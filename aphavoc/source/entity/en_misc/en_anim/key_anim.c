int animate_keyframed_sub_object_type ( object_3d_instance *inst3d, object_3d_sub_object_index_numbers sub_object_type, float t )
{

	object_3d_sub_object_search_data
		search;

	int
		depth,
		found_flag;


	found_flag = FALSE;

	depth = 0;

	while ( TRUE )
	{
		search.search_object = inst3d;
		search.search_depth = depth;
		search.sub_object_index = sub_object_type;

		if ( find_object_3d_sub_object ( &search ) == SUB_OBJECT_SEARCH_RESULT_OBJECT_FOUND )
		{

			found_flag = TRUE;

#ifndef OGRE_EE
			animate_keyframes ( search.result_sub_object, search.object_scene, t );
#else
			animate_keyframes ( inst3d, search.result_sub_object - search.search_object->vp.elements, t );
#endif
		}
		else
		{
			break;
		}

		depth++;
	}

	return found_flag;

}

