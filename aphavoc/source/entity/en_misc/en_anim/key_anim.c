void get_keyframed_animation_state( float value, int *state, float *remainder )
{

	float
		state_float_part,
		state_int_part;

	//
	// returns the current state of the sub-object as an int,
	// plus a float value between 0 and 1 describing how far between the state and its next state
	//

	state_float_part = fast_modf( value, &state_int_part );

	if (state)
	{
		convert_float_to_int( state_int_part, state );
	}

	if (remainder)
	{
		*remainder = state_float_part;
	}
}

