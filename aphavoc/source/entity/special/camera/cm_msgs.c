void overload_camera_message_responses (void)
{
	#if DEBUG_MODULE

	message_responses[ENTITY_TYPE_CAMERA][ENTITY_MESSAGE_LINK_PARENT]	  		= response_to_link_parent;

	message_responses[ENTITY_TYPE_CAMERA][ENTITY_MESSAGE_UNLINK_PARENT] 		= response_to_unlink_parent;

	#endif

	message_responses[ENTITY_TYPE_CAMERA][ENTITY_MESSAGE_LINK_CHILD]	  		= response_to_link_child;

	message_responses[ENTITY_TYPE_CAMERA][ENTITY_MESSAGE_UNLINK_CHILD]		= response_to_unlink_child;

	message_responses[ENTITY_TYPE_CAMERA][ENTITY_MESSAGE_SET_CAMERA_ACTION]	= response_to_set_camera_action;
}

