static void destroy_server_family (entity *en)
{

	destroy_client_server_entity_children (en, LIST_TYPE_GUIDE);

	destroy_client_server_entity_children (en, LIST_TYPE_WAYPOINT);

	destroy_client_server_entity (en);
}

