#ifdef DEBUG

int
   time_acceleration,
	min_time_acceleration = 1,
	max_time_acceleration = 10;

#else

int
   time_acceleration,
	min_time_acceleration = 1,
	max_time_acceleration = 4;

#endif

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int get_time_acceleration (void)
{
	return time_acceleration;
}

int get_min_time_acceleration (void)
{
	return min_time_acceleration;
}

int get_max_time_acceleration (void)
{
	return max (max_time_acceleration, command_line_max_time_acceleration);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_min_time_acceleration (event *ev)
{

   #if DEBUG_MODULE

   debug_log ("FLIGHT: Min time acceleration (%d)", min_time_acceleration);

   #endif

   set_time_acceleration (min_time_acceleration);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_pause_time_acceleration (event *ev)
{

   #if DEBUG_MODULE

   debug_log ("FLIGHT: pause time acceleration (%d)", TIME_ACCELERATION_PAUSE);

   #endif

   set_time_acceleration (TIME_ACCELERATION_PAUSE);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_big_inc_time_acceleration (event *ev)
{

	int
		accel;

   accel = time_acceleration + 20;

   #if DEBUG_MODULE

   debug_log ("FLIGHT: time acceleration = %d", time_acceleration);

   #endif

   set_time_acceleration (accel);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_small_inc_time_acceleration (event *ev)
{

	int
		accel;

   accel = time_acceleration + 1;

   #if DEBUG_MODULE

   debug_log ("FLIGHT: time acceleration = %d", time_acceleration);

   #endif

   set_time_acceleration (accel);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_small_dec_time_acceleration (event *ev)
{

	int
		accel;

   accel = time_acceleration - 1;

   #if DEBUG_MODULE

   debug_log ("FLIGHT: time acceleration = %d", time_acceleration);

   #endif

   set_time_acceleration (accel);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void toggle_pause_time_acceleration (event *ev)
{

	if (time_acceleration == TIME_ACCELERATION_PAUSE)
	{

		set_min_time_acceleration (ev);
	}
	else
	{

		set_pause_time_acceleration (ev);
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_time_acceleration (int acceleration)
{

	if (get_comms_model () == COMMS_MODEL_SERVER)
	{

		if (get_connection_list_head ())
		{

			debug_log ("TIMEACCL: cannot time accelerate multiplayer game");

			return;
		}
	}
	else
	{

		return;
	}

	if ((time_acceleration == TIME_ACCELERATION_PAUSE) && (acceleration != TIME_ACCELERATION_PAUSE))
	{
		continue_sound_system ();
	}
	else if ((time_acceleration != TIME_ACCELERATION_PAUSE) && (acceleration == TIME_ACCELERATION_PAUSE))
	{
		pause_sound_system ();
	}

	time_acceleration = acceleration;

	if (time_acceleration != TIME_ACCELERATION_PAUSE)
	{

		time_acceleration = bound (time_acceleration, min_time_acceleration, get_max_time_acceleration ());
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void set_raw_time_acceleration (int acceleration)
{

	time_acceleration = acceleration;

	time_acceleration = bound (time_acceleration, TIME_ACCELERATION_PAUSE, get_max_time_acceleration ());

	if (time_acceleration == TIME_ACCELERATION_PAUSE)
	{
		pause_sound_system ();
	}
	else
	{
		continue_sound_system ();
	}
}

