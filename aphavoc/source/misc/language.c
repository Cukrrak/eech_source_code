unsigned char
	*language_strings [] =
	{
		"ENGLISH",
		"FRENCH",
		"GERMAN",
		"ITALIAN",
		"SPANISH",
		"RUSSIAN",
		"POLISH",
	};
	
language_struct
	*translation_list,
	*alphabet_pointers [NUM_ALPHABET_POINTERS];

