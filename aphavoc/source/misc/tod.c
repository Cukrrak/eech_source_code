float get_time_of_day (float hours, float minutes, float seconds)
{
	ASSERT ((hours >= 0.0) && (hours < HOURS_IN_A_DAY));

	ASSERT ((minutes >= 0.0) && (minutes < MINUTES_IN_AN_HOUR));

	ASSERT ((seconds >= 0.0) && (seconds < SECONDS_IN_A_MINUTE));

	return ((hours * ONE_HOUR) + (minutes * ONE_MINUTE) + seconds);
}

