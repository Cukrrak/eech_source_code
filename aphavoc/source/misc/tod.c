void get_analogue_clock_values (float time_of_day, float *hours, float *minutes, float *seconds)
{
	time_of_day = bound (time_of_day, 0.0, ONE_DAY - 1.0);

	*hours = time_of_day * ONE_OVER_ONE_HOUR;

	if (*hours >= 12.0)
	{
		*hours -= 12.0;
	}

	*minutes = fmod (time_of_day, ONE_HOUR) * ONE_OVER_ONE_MINUTE;

	*seconds = fmod (time_of_day, ONE_MINUTE);
}

