void get_digital_clock_values (float time_of_day, float *hours, float *minutes, float *seconds)
{
	int
		seconds_past_midnight,
		minutes_past_midnight,
		hours_past_midnight;

//	time_of_day = bound (time_of_day, 0.0, ONE_DAY - 1.0);

	convert_float_to_int (time_of_day, &seconds_past_midnight);

	hours_past_midnight = seconds_past_midnight / ONE_HOUR;

	minutes_past_midnight = seconds_past_midnight / ONE_MINUTE;

	*hours = (float) hours_past_midnight;

	*minutes = (float) (minutes_past_midnight - (hours_past_midnight * MINUTES_IN_AN_HOUR));

	*seconds = (float) (seconds_past_midnight - (minutes_past_midnight * SECONDS_IN_A_MINUTE));
}

