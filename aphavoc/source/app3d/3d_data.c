void increase_3d_resolutions ( event *ev )
{

	int
		index;

	index = get_graphics_resolution_index ( full_screen_width, full_screen_height );

	index += 1;

	if ( index < number_of_graphics_resolution_modes_available )
	{

		set_3d_resolutions ( ( float ) graphics_resolution_modes_available[index].width, ( float ) graphics_resolution_modes_available[index].height );
	}
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void decrease_3d_resolutions ( event *ev )
{

	int
		index;

	index = get_graphics_resolution_index ( full_screen_width, full_screen_height );

	index -= 1;

	if ( index >= 0 )
	{

		set_3d_resolutions ( ( float ) graphics_resolution_modes_available[index].width, ( float ) graphics_resolution_modes_available[index].height );
	}
}

