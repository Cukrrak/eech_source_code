void set_3d_resolutions (float width, float height)
{
	//
	// Validate the resolution now
	// (except when command_line_force_resolution is enabled, then we'll use the resolution
	// whether the gfx card thinks it's supported or not)
	//

	if (!command_line_force_resolution && get_graphics_resolution_index((int)width, (int)height) == -1)
	{

		//
		// Default resolution
		//

		width = 800.0;

		height = 600.0;
	}


	// reset ui fonts

	if ((width != get_global_3d_visual_screen_width ()) ||
		 (height != get_global_3d_visual_screen_height ()))
	{
		set_ui_font_resolution (width, height);

		reinitialise_ui_fonts ();
	}


	// set resolution

	set_global_3d_visual_screen_width (width);
	set_global_3d_visual_screen_height (height);

	// arneh - calulate aspect from width and height, and use aspect to set view angle
	full_screen_aspect_ratio = width / height;
	set_view_angles ( full_screen_virtual_fov );

	full_screen_width			= width;
	full_screen_height		= height;

	int_full_screen_width	= (int) width;
	int_full_screen_height	= (int) height;

	full_screen_x_min			= 0.0;
	full_screen_y_min			= 0.0;
	full_screen_x_mid	  		= width / 2.0;
	full_screen_y_mid	  		= height / 2.0;
	full_screen_x_max	  		= width;
	full_screen_y_max	  		= height;
}

