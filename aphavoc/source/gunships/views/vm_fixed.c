view_modes get_rel_fixed_cockpit_view (int rel_row, int rel_col)
{
	int
		row,
		col;

	row = cockpit_panorama_row + rel_row;
	col = cockpit_panorama_col + rel_col;

	row = bound (row, MIN_COCKPIT_PANORAMA_ROW, MAX_COCKPIT_PANORAMA_ROW);
	col = bound (col, MIN_COCKPIT_PANORAMA_COL, MAX_COCKPIT_PANORAMA_COL);

	return (cockpit_panel_panorama[row][col]);
}

