view_modes get_nearest_fixed_cockpit_view (float heading, float pitch)
{
	int
		row,
		col;

	float
		nearest_error,
		heading_error,
		pitch_error,
		error;

	view_modes
		nearest_mode,
		mode;

	ASSERT (get_apache_havoc_gunship ());

	nearest_error = 1000000000.0;

	nearest_mode = VIEW_MODE_COCKPIT_PANEL_LEVEL_AHEAD;

	for (row = 0; row < NUM_COCKPIT_PANORAMA_ROWS; row++)
	{
		for (col = 0; col < NUM_COCKPIT_PANORAMA_COLS; col++)
		{
			mode = cockpit_panel_panorama[row][col];

			heading_error = heading - pilot_head_heading_values[mode];

			pitch_error = pitch - pilot_head_pitch_values[mode];

			error = (heading_error * heading_error) + (pitch_error * pitch_error);

			if (error < nearest_error)
			{
				nearest_error = error;

				nearest_mode = mode;
			}
		}
	}

	return (nearest_mode);
}

