/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

view_modes get_abs_fixed_cockpit_view (int abs_row, int abs_col)
{
	int
		row,
		col;

	if (abs_row == -1)
	{
		row = MIN_COCKPIT_PANORAMA_ROW;
	}
	else if (abs_row == 1)
	{
		row = MAX_COCKPIT_PANORAMA_ROW;
	}
	else
	{
		row = cockpit_panorama_row;
	}

	if (abs_col == -1)
	{
		col = MIN_COCKPIT_PANORAMA_COL;
	}
	else if (abs_col == 1)
	{
		col = MAX_COCKPIT_PANORAMA_COL;
	}
	else
	{
		col = cockpit_panorama_col;
	}

	return (cockpit_panel_panorama[row][col]);
}

