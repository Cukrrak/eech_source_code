	set_event (DIK_NUMPADENTER, MODIFIER_LEFT_SHIFT, KEY_STATE_DOWN, toggle_lock_target_event);

	set_event (DIK_MULTIPLY, MODIFIER_NONE, KEY_STATE_DOWN, activate_laser_event);

	set_event (DIK_S, MODIFIER_LEFT_CONTROL, KEY_STATE_DOWN, toggle_ground_stabilisation_event);

	//
	// miscellaneous
	//

	set_event (DIK_A, MODIFIER_LEFT_CONTROL, KEY_STATE_DOWN, toggle_ase_auto_page_event);

	set_event (DIK_D, MODIFIER_NONE, KEY_STATE_DOWN, select_next_tsd_declutter_level_event);
	set_event (DIK_D, MODIFIER_LEFT_SHIFT, KEY_STATE_DOWN, select_previous_tsd_declutter_level_event);

	set_event (DIK_E, MODIFIER_NONE, KEY_STATE_DOWN, select_next_tsd_ase_range_event);
	set_event (DIK_E, MODIFIER_LEFT_SHIFT, KEY_STATE_DOWN, select_previous_tsd_ase_range_event);

	set_event (DIK_I, MODIFIER_NONE, KEY_STATE_DOWN, toggle_infra_red_jammer_event);

