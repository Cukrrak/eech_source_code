void draw_left_ng_display_on_texture (void)
{
	set_active_screen (left_ng_screen);

	if (lock_screen (left_ng_screen))
	{
		set_block (0, 0, 33, 33, clear_colour);

		display_left_ng ();

		unlock_screen (left_ng_screen);
	}

	set_active_screen (video_screen);
}

