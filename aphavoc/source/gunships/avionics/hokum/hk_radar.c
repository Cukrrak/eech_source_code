static void rotate_ground_radar_scan_datum_left (void)
{
	ground_radar.scan_datum -= HOKUM_GROUND_RADAR_SCAN_DATUM_ROTATE_STEP;

	if ((ground_radar.scan_datum - (ground_radar.scan_arc_size * 0.5)) >= -HOKUM_RADAR_SCAN_ARC_SIZE_45)
	{
		ground_radar.sweep_offset += HOKUM_GROUND_RADAR_SCAN_DATUM_ROTATE_STEP;
	}
	else
	{
		ground_radar.scan_datum = -HOKUM_RADAR_SCAN_ARC_SIZE_45 + (ground_radar.scan_arc_size * 0.5);
	}

	limit_radar_sweep (&ground_radar);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void fast_rotate_ground_radar_scan_datum_left (void)
{
	float
		theta;

	theta = ground_radar.scan_datum;

	ground_radar.scan_datum = -HOKUM_RADAR_SCAN_ARC_SIZE_45 + (ground_radar.scan_arc_size * 0.5);

	theta = ground_radar.scan_datum - theta;

	ground_radar.sweep_offset -= theta;

	limit_radar_sweep (&ground_radar);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

static void rotate_ground_radar_scan_datum_right (void)
{
	ground_radar.scan_datum += HOKUM_GROUND_RADAR_SCAN_DATUM_ROTATE_STEP;

	if ((ground_radar.scan_datum + (ground_radar.scan_arc_size * 0.5)) <= HOKUM_RADAR_SCAN_ARC_SIZE_45)
	{
		ground_radar.sweep_offset -= HOKUM_GROUND_RADAR_SCAN_DATUM_ROTATE_STEP;
	}
	else
	{
		ground_radar.scan_datum = HOKUM_RADAR_SCAN_ARC_SIZE_45 - (ground_radar.scan_arc_size * 0.5);
	}

	limit_radar_sweep (&ground_radar);
}

