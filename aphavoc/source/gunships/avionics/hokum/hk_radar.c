static void inc_air_radar_scan_range (void)
{
	if (air_radar.scan_range == HOKUM_RADAR_SCAN_RANGE_1000)
	{
		air_radar.scan_range = HOKUM_RADAR_SCAN_RANGE_2000;
	}
	else if (air_radar.scan_range == HOKUM_RADAR_SCAN_RANGE_2000)
	{
		air_radar.scan_range = HOKUM_RADAR_SCAN_RANGE_4000;
	}
	else if (air_radar.scan_range == HOKUM_RADAR_SCAN_RANGE_4000)
	{
		air_radar.scan_range = HOKUM_RADAR_SCAN_RANGE_6000;
	}
	else if (air_radar.scan_range == HOKUM_RADAR_SCAN_RANGE_6000)
	{
		air_radar.scan_range = HOKUM_RADAR_SCAN_RANGE_10000;
	}
}

